		<h1 class="title" itemprop="name">Beijing Design Week 2013</h1>
		
		<div class="article_body" itemprop="articleBody">
			<p>China’s biggest annual design event, the Beijing Design Week, was held for the fourth time this year, from 26 September to 3 October 2013. Despite being design veterans we were “virgins” to the Beijing Design Week and it proved a good experience that offered new input and inspiration in surprising new ways – not so much for the design objects on show but more from the perspective of showcasing design as a creative tool to solve challenges and build awareness and cross-cultural relationships. One aspect we would like to highlight was the city’s art scene, which was highly inspirational and we spent nice and relaxed times walking from gallery to gallery. Last but not least, Thomas Lykke from OeO participated in the panel discussion hosted by Wallpaper* Magazine on 27 September, which aimed to explore whether international design may have something to learn from the richness and diversity of Chinese crafts, traditions and heritage.</p>
