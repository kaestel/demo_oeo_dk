		<h1 itemprop="name">WALLPAPER HANDMADE</h1>

		<div class="description" itemprop="articleBody">
		
			<h2>Bespoke desk for Wallpaper* Handmade 2013</h2>

			<div class="body1 en">
				<p>The Wallpaper* Handmade 2013 spectacle during Salone del Mobile was housed at contemporary gallery Leclettico, where Wallpaper* had assembled an international cast of designers, craftsmen and makers.<br />
OeO collaborated with Stellar Works and Japan Handmade and presented a well-accessorised bespoke desk inspired by the beautiful rituals of reading and writing. The universe included a bespoke desk with inlay fabric produced using a 1200 year old japanese weaving technique, a small tray for writing tools in 2000-year-old lignified Japanese cedar, two precious boxes in copper and wood, a writing mat in refined bamboo, an ipad sleeve in bamboo and leather, a small encrier in black porcelain with exquisite colour glazing, a notebook with Harlequin fabric offering a bold and contemporary new interpretation of the classic pattern – and an ink pen, a magnifying glass and a letter opener with decorative brass knitting. All designed by OeO Design Studio.</p>
			</div>
			<div class="body1 ja" lang="ja">
				<p>The Wallpaper* Handmade 2013は、ミラノサローネ国際家具見本市のコンテンポラリーギャラリーLecletticoにてWallpaper*がセレクトした世界各国のデザイナーや職人、メーカーを紹介する展示として開催されました。<br />
<br />
OeOは、Stellar WorksとJapan Handmadeと共同し、読書と執筆という行為が持つ美しい所作をテーマに鮮やかなディスプレイが施されたオリジナルデスクを展示。デスク周りには、1200年前から続く日本の織技術によるインレー布、樹齢2000年の国産ヒマラヤスギを用いた小さな筆記具用トレイ、銅と木材で出来た愛らしい２つの箱、竹製の上品な下敷き、竹とレザーによるiPadケース、見事な光沢の黒い磁器で作られた小さなインク壷、クラシックな柄に現代的で大胆な解釈を加えたハーレクイーン生地のノート、さらには、インクペン、美しいグラス、銅細工の華やかなレターオープナーなどがディスプレイされました。<br />
デザインはすべてOeO Design Studioによるものです。</p>
			</div>
