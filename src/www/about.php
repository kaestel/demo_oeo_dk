		<p>OeO was founded in 2003 and is a strategic design studio that realises business potential through creative excellence. Inspired by a global perspective, OeO has produced award-winning design and creative solutions for premium brands worldwide within the design, lifestyle and hospitality industries, including Georg Jensen, Velux, Takashimaya, Leica Camera Japan, Sony, Japan Handmade and Copenhagen Airports.</p> 
