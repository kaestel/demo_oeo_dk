		<h1 itemprop="name">STELLAR WORKS COLLECTIONS</h1>

		<div class="description" itemprop="articleBody">
		
			<h2>Comitted to bridging cultures</h2>

			<div class="body1 en">
				<p>Stellar Works is a contemporary design brand with a strong commitment to creating novel collections inspired by craftsmanship and style traditions from around the world. OeO has from the inception of the Stellar Works brand shaped the strategic and creative direction of the brand and is the designer behind the Laval Collection, the SW Collection and the Kyoto Lamp.<br />
<br />
The Laval Collection aims at reinterpreting French style tradition to reflect the poetic simplicity of Japanese and Scandinavian aesthetics. The collection takes its offspring in a collaborative meeting between high-end French furniture making and Scandinavian design and combines the French sense of elegance with a subtle sense of modern comfort. In 2012, The Laval Crown Chair was honored with the ELLE DECO International Design Award in the category ‘Best furniture design’ at a big event held in Shanghai.</p>
			</div>
			<div class="body1 ja" lang="ja">
				<p>Stellar Worksは、クラフトマンシップと各国の伝統的ものづくりからインスピレーションを受けた、コンテンポラリー・デザインブランドです。OeOは彼らの依頼を受け、ブランドの戦略構築とクリエイティブ・ディレクションを行い、Laval Collection、SW Collection、Kyoto Lampにおけるデザインを担当しました。<br />
<br />
Laval Collectionは、伝統的なフレンチスタイルに新しい解釈を加え、日本的な簡素美と北欧的美意識を取り入れたコレクションです。コレクションは高級フランス家具製法と北欧デザインを融合し、フランスが誇る優雅さ、現代的なさりげない使い心地を加えることで生まれました。うちLaval Crown Chairは、2012年上海にて開催されたELLE DECOによる国際デザイン賞にて、“Best furniture Design"を受賞しています。</p>
			</div>
		</div>

