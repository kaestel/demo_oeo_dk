		<div class="description" itemprop="articleBody">
			<div class="body2 en">
				<p>The SW Collection features upholstery furniture designed to embrace the classic values of comfort, while drawing its pure-lined simplicity from the 20th century Bauhaus style. The designs blend effortlessly into any domestic or hospitality environment and the refined craftsmanship ensures that each piece ages with grace.<br />
The Kyoto Lamp is the first of a series of lamps inspired by the traditional woodcraft of the historic houses of Kyoto and by Japanese wooden toy aeroplanes, in particular the Hishou aircraft that dates back to the early years of aviation. The Kyoto lamp is a tribute to perfection, craftsmanship and Kyoto – and is designed to offer warm-toned ambiences with a sense of mystique and shadow play.</p>
			</div>
			<div class="body2 ja" lang="ja">
				<p>SW Collectionは、背もたれ椅子が持つクラシックな座り心地に、20世紀のバウハウス<br />
スタイル的なシンプルなライン美を加えました。優れた職人技巧により、どんなリビングや応接室にも馴染む、使うごとに味わいの増す椅子となっています。<br />
<br />
Kyoto Lampは、京都の歴史建築物に見る伝統的木工技術と、初期の飛行機を忍ばせる日本の“飛翔”を象った木製模型にインスパイアされた、初のランプシリーズです。その完成度とクラフトマンシップ、そして京都に敬意を捧ぐ、神秘的で、影絵のような温もりある<br />
デザインが生まれました。</p>
