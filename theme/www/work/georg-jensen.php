			<div class="body2 en">
				<p>As the Creative Force for Georg Jensen one of OeO’s first undertakings was to develop a new vision and strategy for design. A vision that offered clear direction and a creative framework for new products. OeO optimised the existing product range short term and discovered a number of underexposed treasures in the Georg Jensen product range that failed to perform to their full potential. These OeO turned into new products and collections with commercial potential and high PR value. OeO introduced new materials, new colours and operational design tools for optimizing collaboration with external design partners.</p>
			</div>
			<div class="body2 ja" lang="ja">
				<p>ブランドのクリエイティブ強化のため、OeOはまずデザインに対する新たなビジョンと戦略構築に取りかかりまた。そこで私たちは、新商品に明確なディレクションとクリエイティブな構想を示すビジョンを考案。既存の商品群を短い言葉で言い表すことで、これまでその魅力を十分に発揮できておらず埋もれていた商品達を発見しました。私たちはそれらに商品力と訴求力を加え、新たな商品やコレクションへと蘇らせました。また外部のパートナーと積極的にコラボレーションをし、新しい素材や色、デザイン操作ツールなども導入しました。</p>
			</div>
